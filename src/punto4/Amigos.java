package punto4;

public class Amigos {

	public String nombre;
	public String apellido;
	public int edad;
	public String sexo;
	public String direccion;
	public int telefono;
	
	public Amigos(String nombre, String apellido, int edad, String sexo, String direccion, int telefono)
	{
		this.nombre = nombre;
		this.apellido = apellido;
		this.edad = edad;
		this.sexo = sexo;
		this.direccion = direccion;
		this.telefono = telefono;
	}
	
}
